// ** SWITCH CASE
var a = 8;

switch (a) {
  case 10:
  case 9:
  case 8:
    console.log("10 or 9 or 8");
    break;

  case 7:
    console.log("7");
    break;

  default:
    console.log("none of above");
    break;
}
